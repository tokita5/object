package asobiba;

public class Hairetu {
    public static void main(String[] args) {
        //空の配列を宣言
        int[] array = new int[3];//サイズ3の整数型配列を宣言

        //値の代入
        array[0] = 10; //インデックス０の値の中に10を代入
        array[1] = 20; //インデックス1の値の中に20を代入
        array[2] = 30; //インデックス2の値の中に30を代入

        System.out.println(array[0]);//10
        System.out.println(array[1]);//20
        System.out.println(array[2]);//30
    }
}
